package org.oppia.android.testing

import org.oppia.android.util.system.UserIdGenerator
import javax.inject.Inject
import javax.inject.Singleton

/** A test specific fake for UUID. */
@Singleton
class FakeUUIDImpl @Inject constructor() : UserIdGenerator {

  private var currentUUIDValue = "default_uuid_value"

  override fun generateRandomUserId(): String = currentUUIDValue

  /**
   * Sets the current UUID string to [value].
   * This string would be returned by the [UserIdGenerator] when asked for a random UUID string.
   */
  fun setUUIDValue(value: String) {
    currentUUIDValue = value
  }

  /**
   * Returns the current UUID string which is generated by the [UserIdGenerator] when asked for a
   * random UUID string.
   */
  fun getUUIDValue(): String = currentUUIDValue
}
